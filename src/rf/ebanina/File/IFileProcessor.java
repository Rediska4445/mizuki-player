package rf.ebanina.File;

/**
 * Общий интерфейс для работы с файловыми значениями.
 * <p>
 * Предоставляет базовые операции чтения и записи произвольных значений
 * по строковому пути и ключам трека/типа. Предназначен главным образом
 * для работы с кэшем и общими файловыми операциями, не связаными с
 * аудио-метаданными (для них существуют отдельные интерфейсы).
 */
public interface IFileProcessor
{
    /**
     * Сохраняет значение в файл или кэш по указанному пути и ключам.
     *
     * @param path  базовый путь или идентификатор файла/директории
     * @param track дополнительный ключ, обычно идентификатор трека
     * @param type  тип данных или логический под-ключ (например, "cover", "lrc")
     * @param value значение для сохранения; конкретный формат зависит от реализации
     * @param <T>   тип сохраняемого значения
     */
    <T> void save(String path, String track, String type, T value);
    /**
     * Читает значение из файла или кэша по указанному пути и ключам.
     * <p>
     * Если значение отсутствует или не может быть прочитано,
     * возвращается значение по умолчанию {@code ifNull}.
     *
     * @param path   базовый путь или идентификатор файла/директории
     * @param track  дополнительный ключ, обычно идентификатор трека
     * @param type   тип данных или логический под-ключ (например, "cover", "lrc")
     * @param ifNull значение по умолчанию, возвращаемое при отсутствии данных
     * @param <T>    ожидаемый тип возвращаемого значения
     * @return прочитанное значение или {@code ifNull}, если данных нет
     */
    <T> T read(String path, String track, String type, T ifNull);
    /**
     * Преобразует или нормализует имя для использования в файловой системе
     * или в качестве ключа кэша.
     *
     * @param name исходное имя
     * @return нормализованное имя, безопасное для использования в выбранном хранилище
     */
    String name(String name);
}