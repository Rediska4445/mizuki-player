package rf.ebanina.File;

/**
 * Базовый интерфейс ссылочного файла или ресурса.
 * <p>
 * Описывает объект, который может предоставить свой путь
 * в файловой системе или внутри абстрактного файлового хранилища.
 */
public interface Reference
{
    /**
     * Возвращает путь к ресурсу.
     * <p>
     * Формат пути определяется реализацией: это может быть
     * обычный файловый путь, виртуальный путь внутри архива,
     * сетевого хранилища и т.п.
     *
     * @return строковое представление пути к ресурсу,
     *         никогда не {@code null}
     */
    String getPath();
}
