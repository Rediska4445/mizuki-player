package rf.ebanina.ebanina.Player.Controllers;

import javafx.scene.image.Image;
import javafx.scene.paint.Color;
import rf.ebanina.ebanina.Player.Track;
/**
 * <h1>IArtProcessor</h1>
 * Интерфейс для динамического обновления цветовой гаммы приложения на основе обложки трека.
 * <p>
 * Служит контрактом для процессоров анализа изображений обложек, которые извлекают
 * доминирующие цвета и синхронизируют цветовую схему UI с музыкальным контентом.
 * </p>
 *
 * <h3>Сценарий использования:</h3>
 * <p>
 * При смене трека в плеере:
 * </p>
 * <ol>
 *   <li>Загружается обложка трека ({@link Track})</li>
 *   <li>Анализируется цветовая палитра изображения</li>
 *   <li>Обновляется цветовая схема приложения</li>
 * </ol>
 *
 * <h3>Типичные реализации:</h3>
 * <ul>
 *   <li>Извлечение доминирующего цвета</li>
 *   <li>Выбор гармоничной палитры (аналогичные/комплементарные цвета)</li>
 *   <li>Генерация градиентов на основе обложки</li>
 *   <li>Кэширование цветовых профилей треков</li>
 * </ul>
 *
 * <h3>Пример использования:</h3>
 * <pre>{@code
 * IArtProcessor artProcessor = new DominantColorProcessor();
 *
 * // Инициализация при старте плеера
 * artProcessor.initArt(currentTrack);
 *
 * // Обновление при смене трека
 * artProcessor.initArt(newTrack);
 *
 * // Ручное задание цвета
 * artProcessor.updateColors(extractedColor);
 * }</pre>
 *
 * @author Ebanina Std
 * @since 1.4.9
 * @see Track
 */
public interface IArtProcessor
{
    /**
     * Обновляет цветовую схему приложения на основе заданного цвета.
     * <p>
     * Применяет цвет ко всем UI элементам: акценты, градиенты, тени, фоны.
     * </p>
     *
     * @param color основной цвет для цветовой схемы
     */
    void updateColors(Color color);
    /**
     * Инициализирует цветовую схему на основе анализа изображения.
     * <p>
     * Автоматически извлекает доминирующий цвет или палитру из изображения.
     * </p>
     *
     * @param image обложка трека для анализа
     */
    void initColor(Image image);
    /**
     * Инициализирует процессор обложкой трека.
     * <p>
     * Полный цикл: загрузка изображения → анализ цветов → применение схемы.
     * </p>
     *
     * @param path объект трека с обложкой
     */
    void initArt(Track path);
    /**
     * Устанавливает изображение обложки напрямую.
     * <p>
     * Обходит анализ Track объекта для прямой работы с Image.
     * </p>
     *
     * @param img готовое изображение обложки
     */
    void setImage(Image img);
}
